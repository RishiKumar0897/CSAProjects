//   Prints a table of student grades  
// *************************************************************** 

public class TableOfStudentGrades {
  public static void main (String[] args)
  {
    System.out.println ("////////////////////\\\\\\\\\\\\\\\\\\\\");
    System.out.println ("==\tStudent Points\t=="); 
    System.out.println ("\\\\\\\\\\\\\\\\\\\\////////////////////");
    System.out.println ("Name\t\tLab\tBonus\tTotal");
    System.out.println ("----\t---\t-----\t-----"); 
    System.out.println ("Joe\t\t43\t7" + "\t" + (43+7));
    System.out.println ("William\t50\t8" + "\t" + (50+8));
    System.out.println ("Mary Sue\t39\t10" + "\t" + (39+10));
    System.out.println ("Rishi\t\t56\t3" + "\t" + (56+3));
    System.out.println ("Steve\t\t36\t12" + "\t" + (36+12));
  }
}